import { requestIdentitypProof } from "../passport";

export const OpenPassportRequest = ({
  msgBigInt,
  modulusBigInt,
  sigBigInt,
  validInputs,
}: {
  msgBigInt: bigint;
  modulusBigInt: bigint;
  sigBigInt: bigint;
  validInputs: boolean;
}) => {
  return (
    <button
      onClick={() => {
        if (msgBigInt && modulusBigInt && sigBigInt) {
          requestIdentitypProof({
            msgBigInt,
            modulusBigInt,
            sigBigInt,
          });
        }
      }}
      disabled={validInputs}
    >
      Request Identity Proof
    </button>
  );
};
